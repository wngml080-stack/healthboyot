-- [1단계] 내가 생성한 배정+세션 삭제 (세션은 CASCADE로 함께 삭제됨)
-- =============================================
DELETE FROM ot_assignments
WHERE created_at >= '2026-03-26 08:45:00+00'
AND created_at <= '2026-03-26 08:46:00+00';

-- 내가 생성한 회원도 삭제 (같은 시간대에 생성된 회원)
DELETE FROM members
WHERE created_at >= '2026-03-26 08:45:00+00'
AND created_at <= '2026-03-26 08:46:00+00'
AND phone IN (
  '010-4180-7604','010-9620-1818','010-4558-8356','010-9241-6495',
  '010-7673-3239','010-4555-6897','010-9314-5082','010-9108-8421',
  '010-5295-8935','010-6384-7701','010-5893-9595','010-9489-0651',
  '010-9338-3598','010-5519-7654','010-2666-8526','010-6623-3762',
  '010-8339-6224','010-2533-8908','010-5027-3117'
);

-- =============================================
-- [2단계] 확인: 기존 회원들이 잘 남아있는지 체크
-- =============================================
SELECT m.id, m.name, m.phone, m.created_at,
  (SELECT COUNT(*) FROM ot_assignments oa WHERE oa.member_id = m.id) AS 배정수
FROM members m
WHERE m.name IN ('이서윤','박은진','정현진','이현숙','조현명','김세영','서정원','서기윤','박준영','김혜원','김지현','강성은','조아란','장세준','강영준','김윤지','김은환','하동수','권세윤')
ORDER BY m.name;
